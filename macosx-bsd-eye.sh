info "Creating Openbox autostart..."
cat > "$USER_HOME/.config/openbox/autostart" <<EOF
#!/bin/sh

# Load xterm/font colors.
if command -v xrdb >/dev/null 2>&1; then
  xrdb -merge "\$HOME/.Xresources" &
fi

# Nice cursor.
xsetroot -cursor_name left_ptr 2>/dev/null || true

# Wallpaper.
if command -v feh >/dev/null 2>&1; then
  feh --bg-fill "\$HOME/Pictures/${THEME_NAME}-wallpaper.ppm" &
else
  xsetroot -solid "$ACCENT" &
fi

# Shadows/transparency/fading.
if command -v picom >/dev/null 2>&1; then
  picom --config "\$HOME/.config/picom/picom.conf" &
fi

# Dock-like bottom panel.
if command -v tint2 >/dev/null 2>&1; then
  tint2 -c "\$HOME/.config/tint2/tint2rc" &
fi

# VirtualBox clipboard integration.
if command -v VBoxClient >/dev/null 2>&1; then
  VBoxClient --clipboard &
  VBoxClient --draganddrop &
  VBoxClient --seamless &
  VBoxClient --checkhostversion &
fi

# First terminal as safety net.
xterm &
EOF

