#!/bin/sh
# macosx-bsd-eye.sh
# Aqua/Panther/Tiger-inspired eye-candy preset for FreeBSD + X11 + Openbox.
#
# Usage as root:
#   sh macosx-bsd-eye.sh USER [aqua|graphite|tiger]
#
# Example:
#   sh macosx-bsd-eye.sh w3r aqua
#
# Then login as USER and run:
#   startx
#
# This script does NOT use Apple assets. It only creates Aqua-inspired colors/configs.

set -eu

die() {
  echo "ERROR: $*" >&2
  exit 1
}

info() {
  echo "==> $*"
}

warn() {
  echo "WARN: $*" >&2
}

[ "$(uname -s)" = "FreeBSD" ] || die "This script is intended for FreeBSD."
[ "$(id -u)" -eq 0 ] || die "Run as root: sh $0 USER [aqua|graphite|tiger]"

USER_NAME="${1:-}"
PROFILE="${2:-aqua}"

[ -n "$USER_NAME" ] || die "Usage: sh $0 USER [aqua|graphite|tiger]"
id "$USER_NAME" >/dev/null 2>&1 || die "User '$USER_NAME' does not exist."

USER_HOME="$(pw usershow "$USER_NAME" | awk -F: '{print $9}')"
[ -d "$USER_HOME" ] || die "Home directory not found: $USER_HOME"

case "$PROFILE" in
  aqua)
    THEME_NAME="AquaBSD"
    TOP="#d7ecff"
    MID="#69aef5"
    BOT="#1b64b0"
    TITLE_TEXT="#ffffff"
    INACTIVE_TOP="#eeeeee"
    INACTIVE_BOT="#bfc8d0"
    WALL_R1=38;  WALL_G1=95;  WALL_B1=175
    WALL_R2=128; WALL_G2=192; WALL_B2=255
    PANEL="#eef7ff"
    ACCENT="#2b83d3"
    ;;
  graphite)
    THEME_NAME="GraphiteBSD"
    TOP="#f2f4f6"
    MID="#bfc8d1"
    BOT="#6d7b89"
    TITLE_TEXT="#ffffff"
    INACTIVE_TOP="#eeeeee"
    INACTIVE_BOT="#c7c7c7"
    WALL_R1=62;  WALL_G1=70;  WALL_B1=82
    WALL_R2=186; WALL_G2=198; WALL_B2=212
    PANEL="#f5f6f7"
    ACCENT="#6d7b89"
    ;;
  tiger)
    THEME_NAME="TigerBSD"
    TOP="#e7f5ff"
    MID="#71c5ff"
    BOT="#0e75c9"
    TITLE_TEXT="#ffffff"
    INACTIVE_TOP="#f6f6f6"
    INACTIVE_BOT="#d4d4d4"
    WALL_R1=8;   WALL_G1=62;  WALL_B1=124
    WALL_R2=94;  WALL_G2=189; WALL_B2=255
    PANEL="#f8fbff"
    ACCENT="#0e75c9"
    ;;
  *)
    die "Unknown profile '$PROFILE'. Use: aqua, graphite, tiger"
    ;;
esac

install_pkg() {
  PKG="$1"
  if pkg info -e "$PKG" >/dev/null 2>&1; then
    return 0
  fi
  info "Installing package: $PKG"
  if ! pkg install -y "$PKG"; then
    warn "Could not install '$PKG'. Continuing anyway."
  fi
}

info "Bootstrapping pkg if needed..."
env ASSUME_ALWAYS_YES=yes pkg bootstrap -f >/dev/null 2>&1 || true

info "Updating pkg metadata..."
pkg update || warn "pkg update failed; trying to continue."

info "Installing eye-candy packages..."
install_pkg openbox
install_pkg xterm
install_pkg picom
install_pkg feh
install_pkg tint2
install_pkg lxappearance
install_pkg fontconfig
install_pkg dejavu
install_pkg webfonts

TS="$(date +%Y%m%d-%H%M%S)"

backup_path() {
  P="$1"
  if [ -e "$P" ]; then
    cp -Rp "$P" "$P.backup.$TS"
    info "Backup: $P.backup.$TS"
  fi
}

backup_path "$USER_HOME/.config/openbox"
backup_path "$USER_HOME/.config/picom"
backup_path "$USER_HOME/.config/tint2"
backup_path "$USER_HOME/.themes/$THEME_NAME"
backup_path "$USER_HOME/.Xresources"
backup_path "$USER_HOME/.gtkrc-2.0"
backup_path "$USER_HOME/.config/gtk-3.0/settings.ini"

info "Creating directories..."
mkdir -p \
  "$USER_HOME/.config/openbox" \
  "$USER_HOME/.config/picom" \
  "$USER_HOME/.config/tint2" \
  "$USER_HOME/.config/gtk-3.0" \
  "$USER_HOME/.themes/$THEME_NAME/openbox-3" \
  "$USER_HOME/Pictures"

info "Generating Aqua-like wallpaper..."
WALL="$USER_HOME/Pictures/${THEME_NAME}-wallpaper.ppm"
awk -v w=1280 -v h=800 \
    -v r1="$WALL_R1" -v g1="$WALL_G1" -v b1="$WALL_B1" \
    -v r2="$WALL_R2" -v g2="$WALL_G2" -v b2="$WALL_B2" '
BEGIN {
  print "P3";
  print w " " h;
  print "255";
  for (y = 0; y < h; y++) {
    t = y / (h - 1);
    # Vertical Aqua-ish gradient.
    r = int(r1 * (1 - t) + r2 * t);
    g = int(g1 * (1 - t) + g2 * t);
    b = int(b1 * (1 - t) + b2 * t);

    # Soft bright horizontal band, like old shiny wallpapers.
    band = exp(-((t - 0.34) * (t - 0.34)) / 0.008) * 42;
    r = r + band; g = g + band; b = b + band;

    # Slight darker bottom.
    if (t > 0.72) {
      d = (t - 0.72) * 55;
      r -= d; g -= d; b -= d;
    }

    if (r < 0) r = 0; if (g < 0) g = 0; if (b < 0) b = 0;
    if (r > 255) r = 255; if (g > 255) g = 255; if (b > 255) b = 255;

    for (x = 0; x < w; x++) {
      # faint radial shine
      dx = (x - w * 0.72) / w;
      dy = (y - h * 0.18) / h;
      shine = exp(-(dx * dx + dy * dy) / 0.035) * 28;

      rr = int(r + shine); gg = int(g + shine); bb = int(b + shine);
      if (rr > 255) rr = 255; if (gg > 255) gg = 255; if (bb > 255) bb = 255;

      printf "%d %d %d ", rr, gg, bb;
    }
    printf "\n";
  }
}' > "$WALL"

